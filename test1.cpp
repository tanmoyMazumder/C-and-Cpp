#include<bits/stdc++.h>

using namespace std;

int main()
{
    int n=6;
    for(int i=0;i<n;i++)
    {
        for(int j=i;j>=0;j--)

            cout<<j;
            cout<<"\n";

    }
    for(int i=n-2;i>=0;i--)
    {
        for(int j=i;j>=0;j--)

            cout<<j;
            cout<<"\n";

    }




    return 0;
}












